// Arrays

// An array is typically going to be the most basic form of data structure that you'll find in any given programming language. An array is an index-based linear collection of elements. More often than not, we collect items of similar data types within an array, which makes it easier to work with the data.

// When working with arrays, it is important to take note of two critical terms: 

// Index: the location of a piece of data in the array

// Value: the value at that index

// arrays are often indexed at 0, which means that if you want to access the Nth element in the array, the syntax for retrieving that element is array[N-1]


